import UIKit

struct Utils {
	static func regularFont() -> UIFont {
		return UIFont.systemFont(ofSize: 12.0)
	}
	
	static func borderColor() -> UIColor {
		return UIColor(red: 136.0/255.0, green: 186.0/255.0, blue: 136.0/255.0, alpha: 1.0)
	}
}
